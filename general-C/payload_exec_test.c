void handle_signal(int signal, siginfo_t *info, void *context) {
    switch (signal) {
        case SIGSEGV:
            fprintf(stderr, "Error: Segmentation fault occurred at address %p\n", info->si_addr);
            break;
        case SIGILL:
            fprintf(stderr, "Error: Illegal instruction encountered at address %p\n", info->si_addr);
            break;
        case SIGFPE:
            fprintf(stderr, "Error: Floating point exception at address %p\n", info->si_addr);
            break;
        case SIGABRT:
            fprintf(stderr, "Error: Process aborted\n");
            break;
        case SIGBUS:
            fprintf(stderr, "Error: Bus error at address %p\n", info->si_addr);
            break;
        default:
            fprintf(stderr, "Error: Signal %d received at address %p\n", signal, info->si_addr);
    }
    exit(EXIT_FAILURE);
}

void setup_signal_handlers() {
    struct sigaction sa;
    sa.sa_sigaction = handle_signal;
    sa.sa_flags = SA_SIGINFO | SA_RESTART;

    sigaction(SIGSEGV, &sa, NULL);
    sigaction(SIGILL, &sa, NULL);
    sigaction(SIGFPE, &sa, NULL);
    sigaction(SIGABRT, &sa, NULL);
    sigaction(SIGBUS, &sa, NULL);
}

int make_memory_executable(void *addr, size_t length) {
    long page_size = sysconf(_SC_PAGESIZE);
    if (page_size == -1) {
        perror("Error getting page size");
        exit(EXIT_FAILURE);
    }

    void *aligned_addr = (void *)((uintptr_t)addr & ~(page_size - 1));
    size_t adjusted_length = length + ((uintptr_t)addr - (uintptr_t)aligned_addr);

    if (mprotect(aligned_addr, adjusted_length, PROT_READ | PROT_WRITE | PROT_EXEC) != 0) {
        perror("Error making memory executable");
        exit(EXIT_FAILURE);
    }
    return 0;
}

