// Signal handler for catching segmentation faults
void handle_signal(int signal) {
    if (signal == SIGSEGV) {
        fprintf(stderr, "Error: Segmentation fault occurred during shellcode execution\n");
    }
    exit(EXIT_FAILURE);
}

// Function to align the address to the nearest page boundary
void make_memory_executable(void *addr, size_t length) {
    // Get the page size of the system
    long page_size = sysconf(_SC_PAGESIZE);
    if (page_size == -1) {
        perror("Error getting page size");
        exit(EXIT_FAILURE);
    }

    // Align the address to the page boundary
    void *aligned_addr = (void *)((uintptr_t)addr & ~(page_size - 1));

    // Adjust length to cover the full pages
    size_t adjusted_length = length + ((uintptr_t)addr - (uintptr_t)aligned_addr);

    // Call mprotect on the aligned address and adjusted length
    if (mprotect(aligned_addr, adjusted_length, PROT_READ | PROT_WRITE | PROT_EXEC) != 0) {
        perror("Error making memory executable");
        exit(EXIT_FAILURE);
    }
}

